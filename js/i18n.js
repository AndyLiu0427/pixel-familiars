let lang = 'zh-TW';

export function detectLang() {
  const nav = (typeof navigator !== 'undefined' && navigator.language) || 'en';
  return nav.toLowerCase().startsWith('zh') ? 'zh-TW' : 'en';
}

export function setLang(l) { if (STRINGS[l]) lang = l; }
export function getLang() { return lang; }

export function t(key, vars) {
  let s = (STRINGS[lang] && STRINGS[lang][key]) ?? STRINGS.en[key] ?? key;
  if (Array.isArray(s)) return s;
  if (vars) for (const [k, v] of Object.entries(vars)) s = s.replaceAll(`{${k}}`, v);
  return s;
}

// Localized name for data entries shaped { name: { 'zh-TW': .., en: .. } }
export function tn(entry) {
  return entry.name[lang] ?? entry.name.en;
}
